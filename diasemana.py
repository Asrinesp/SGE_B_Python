dia_semana = input("Introduce un dia de la semana: ")
if dia_semana == "lunes":
    print("Hoy es lunes")
elif dia_semana == "martes":
    print("Hoy es martes")
elif dia_semana == "miercoles":
    print("Hoy es miercoles")
elif dia_semana == "jueves":
    print("Hoy es jueves")
elif dia_semana == "viernes":
    print("Hoy es viernes")
elif dia_semana == "sabado":
    print("Hoy es sabado")
elif dia_semana == "domingo":
    print("Hoy es domingo")
else:
    print("Introduce un dia de la semana correcto en minusculas")
